<script setup lang="ts">
/**
 * SystemStatusCards —— 系统状态监控卡片
 *
 * 📝 Responsibilities:
 *  1. 显示各系统组件运行状态
 *  2. 提供组件启停控制
 *  3. 展示关键运行指标
 *  4. 错误信息提示
 *  5. 系统健康度概览
 *
 * 📦 Dependencies:
 *  - 系统监控API
 *  - 日期格式化工具
 *
 * 🔄 Update Log:
 *  - 2025-07-27  初始创建
 */

import { ref, computed, onMounted, onUnmounted } from 'vue'
import { ElMessage, ElMessageBox } from 'element-plus'
import { 
  CircleCheck, 
  Warning, 
  CircleClose, 
  Loading, 
  Remove, 
  Refresh 
} from '@element-plus/icons-vue'

import { formatDateTime, formatUptime } from '@/utils/date'
import { formatFileSize } from '@/utils/format'
import { systemApi } from '@/api'

// ===== Props & Emits =====
defineProps<{
  loading?: boolean
}>()

const emit = defineEmits<{
  'refresh': []
}>()

// ===== 响应式数据 =====
const systemComponents = ref<any[]>([])
const loading = ref(false)

// ===== 计算属性 =====
const runningCount = computed(() => 
  systemComponents.value.filter(c => c.status === 'running').length
)

const warningCount = computed(() => 
  systemComponents.value.filter(c => c.status === 'warning').length
)

const errorCount = computed(() => 
  systemComponents.value.filter(c => c.status === 'error').length
)

const stoppedCount = computed(() => 
  systemComponents.value.filter(c => c.status === 'stopped').length
)

const systemHealthPercentage = computed(() => {
  const total = systemComponents.value.length
  const healthy = runningCount.value + warningCount.value * 0.5
  return Math.round((healthy / total) * 100)
})

const systemHealthStatus = computed(() => {
  const percentage = systemHealthPercentage.value
  if (percentage >= 90) return 'success'
  if (percentage >= 70) return 'warning'
  return 'exception'
})

// ===== 方法 =====

/**
 * 获取系统组件状态
 */
async function fetchSystemComponents() {
  try {
    loading.value = true
    const response = await systemApi.getComponentStatus()
    systemComponents.value = response.data.map((component: any) => ({
      ...component,
      loading: false
    }))
  } catch (error) {
    console.error('获取系统组件状态失败:', error)
    ElMessage.error('获取系统状态失败')
  } finally {
    loading.value = false
  }
}

/**
 * 获取状态标签类型
 */
function getStatusTagType(status: string): string {
  const typeMap: Record<string, string> = {
    'running': 'success',
    'warning': 'warning', 
    'error': 'danger',
    'starting': 'info',
    'stopped': 'info'
  }
  return typeMap[status] || 'info'
}

/**
 * 获取状态文本
 */
function getStatusText(status: string): string {
  const textMap: Record<string, string> = {
    'running': '运行中',
    'warning': '警告',
    'error': '错误',
    'starting': '启动中',
    'stopped': '已停止'
  }
  return textMap[status] || status
}

/**
 * 格式化内存大小
 */
function formatMemory(bytes: number): string {
  return formatFileSize(bytes)
}

/**
 * 启动组件
 */
async function startComponent(componentName: string) {
  const component = systemComponents.value.find(c => c.name === componentName)
  if (!component) return
  
  try {
    await ElMessageBox.confirm(
      `确定要启动 "${component.description}" 服务吗？`,
      '确认启动',
      {
        type: 'info',
        confirmButtonText: '启动',
        cancelButtonText: '取消'
      }
    )
    
    component.loading = true
    
    const response = await systemApi.startComponent(componentName)
    
    if (response.success) {
      component.status = 'running'
      component.lastCheck = new Date().toISOString()
      ElMessage.success(`${component.description} 启动成功`)
      await fetchSystemComponents() // 刷新状态
    } else {
      throw new Error(response.message || '启动失败')
    }
    
  } catch (error: any) {
    if (error !== 'cancel') {
      console.error('启动服务失败:', error)
      ElMessage.error(error.message || '启动服务失败')
    }
  } finally {
    component.loading = false
  }
}

/**
 * 停止组件
 */
async function stopComponent(componentName: string) {
  const component = systemComponents.value.find(c => c.name === componentName)
  if (!component) return
  
  try {
    await ElMessageBox.confirm(
      `确定要停止 "${component.description}" 服务吗？`,
      '确认停止',
      {
        type: 'warning',
        confirmButtonText: '停止',
        cancelButtonText: '取消',
        confirmButtonClass: 'el-button--danger'
      }
    )
    
    component.loading = true
    
    const response = await systemApi.stopComponent(componentName)
    
    if (response.success) {
      component.status = 'stopped'
      component.lastCheck = new Date().toISOString()
      ElMessage.success(`${component.description} 停止成功`)
      await fetchSystemComponents() // 刷新状态
    } else {
      throw new Error(response.message || '停止失败')
    }
    
  } catch (error: any) {
    if (error !== 'cancel') {
      console.error('停止服务失败:', error)
      ElMessage.error(error.message || '停止服务失败')
    }
  } finally {
    component.loading = false
  }
}

/**
 * 重启组件
 */
async function restartComponent(componentName: string) {
  const component = systemComponents.value.find(c => c.name === componentName)
  if (!component) return
  
  try {
    await ElMessageBox.confirm(
      `确定要重启 "${component.description}" 服务吗？`,
      '确认重启',
      {
        type: 'warning',
        confirmButtonText: '重启',
        cancelButtonText: '取消'
      }
    )
    
    component.loading = true
    component.status = 'starting'
    
    const response = await systemApi.restartComponent(componentName)
    
    if (response.success) {
      ElMessage.success(`${component.description} 重启成功`)
      await fetchSystemComponents() // 刷新状态
    } else {
      throw new Error(response.message || '重启失败')
    }
    
  } catch (error: any) {
    if (error !== 'cancel') {
      console.error('重启服务失败:', error)
      ElMessage.error(error.message || '重启服务失败')
    }
  } finally {
    component.loading = false
  }
}

/**
 * 查看日志
 */
function viewLogs(componentName: string) {
  ElMessage.info(`查看 ${componentName} 日志功能开发中...`)
}

/**
 * 刷新所有组件状态
 */
async function refreshAll() {
  emit('refresh')
  await fetchSystemComponents()
}

// ===== 生命周期 =====
onMounted(async () => {
  // 初始加载系统组件状态
  await fetchSystemComponents()
  
  // 启动定时刷新（每30秒）
  const refreshInterval = setInterval(() => {
    fetchSystemComponents()
  }, 30000)
  
  // 清理定时器
  onUnmounted(() => {
    clearInterval(refreshInterval)
  })
})
</script>

